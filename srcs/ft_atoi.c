#include "../libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int sign;
	int num;

	i = 0;
	sign = 1;
	num = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '+')
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		num = num + (str[i] - '0');
		if (ft_isdigit(str[i + 1]))
			num *= 10;
		i++;
	}
	return (num * sign);
}
